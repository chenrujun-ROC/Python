# while迴圈
n=1
sum=0
while n<=10:
    sum=sum+n
    n+=1
    print(sum)
# for迴圈
#印出列表
for x in [3,5,1,6]:
    print(x)
#印出字串中個別字元
for x in "hello":
    print(x)
#印出0-4的列表
for x in range(5):
    print(x)
#印出5-(10-1)的列表
for x in range(5,10):
    print(x)
#1+2+..+9+10
